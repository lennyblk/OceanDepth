void display_creature_ascii(int creature_type)
{
    switch (creature_type)
    {
    case CREATURE_KRAKEN:
        display_kraken_ascii();
        break;
    case CREATURE_SHARK:
        display_shark_ascii();
        break;
    case CREATURE_JELLYFISH:
        display_jellyfish_ascii();
        break;
    case CREATURE_SWORDFISH:
        display_swordfish_ascii();
        break;
    case CREATURE_GIANT_CRAB:
        display_giant_crab_ascii();
        break;
    default:
        printf(COLOR_WHITE "Créature inconnue\n" COLOR_RESET);
        break;
    }
}

